void print(int *pBuf, int size)
{
    assert(NULL != pBuf);
    assert(0 < size);

    int i = 0;
    int iMax = 0;

    iMax = size;
    for(i=0;i<iMax;i++)
    {
        printf("%d,", pBuf[i]);
    }
    printf("\n");
}

