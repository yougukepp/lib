int gcd(int m, int n)
{
  assert(m>0);
  assert(n>0);

  int tmp = 0;
  int r = 0;

  /*
   * 排序 用于提升性能
   */
  if(m<n)
  {
    tmp = m;
    m = n;
    n = tmp;
  }

  while(true)
  {
    r = m % n;

    if(0 == r)
    {
       return n;
    }
    else
    {
        m = n;
        n = r;
    }

  }

  return 1;
}

