unsigned long long factorial(int n)
{
  assert(n>0);

  unsigned long long rst = 0;
  int i = 0;
  int iMax = 0;


  if(1 == n)
  {
      rst = 1.0;
      return rst;
  }

  rst = 1.0;
  iMax = n + 1;
  for(i=2;i<iMax;i++)
  {
      rst *= i;
  }

  return rst;
}

